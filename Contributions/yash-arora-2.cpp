//program to take two numbers as input and return their sum, and product.

#include <iostream>
#include <math.h>
#include<string>
using namespace std;

int main()
{
	int a;
	int b;
	cout<<"Enter the first number: ";
	cin>>a;
	cout<<"Enter the second number: ";
	cin>>b;
	cout<<"The sum of the two numbers is: "<<a+b<<endl;
	cout<<"The product of the two numbers is: "<<a*b<<endl;
	return 0;
}
